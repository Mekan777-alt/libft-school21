#include "libft.h"

size_t	ft_strlcpy(char *dst, char *src, size_t dstsize)
{
	size_t	i;
	size_t	len;

	if (dst == NULL || src == NULL)
		return (0);
	len = ft_strlen(src);
	if (dstsize != 0)
	{
		i = 0;
		while ((i < len) && i < (dstsize - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (len);
}
